def build() -> None:
    os.makedirs(ROOT, exist_ok=True)

    template = Catalog(project="SQLAdmin", domain="admin")
    for message in MESSAGES:
        template.add(message, string="")
    with open(os.path.join(ROOT, "admin.pot"), "wb") as f:
        write_po(f, template, omit_header=False)

    for locale, mapping in TRANSLATIONS.items():
        catalog = copy.deepcopy(template)
        catalog.locale = locale
        catalog.fuzzy = False
        for message in catalog:  # type: ignore[assignment]
            if message.id and message.id in mapping:  # type: ignore[attr-defined]
                message.string = mapping[message.id]  # type: ignore[attr-defined]

        directory = os.path.join(ROOT, locale, "LC_MESSAGES")
        os.makedirs(directory, exist_ok=True)
        with open(os.path.join(directory, "admin.po"), "wb") as f:
            write_po(f, catalog, omit_header=False, sort_output=False)
        with open(os.path.join(directory, "admin.mo"), "wb") as f:
            write_mo(f, catalog)
        translated = sum(1 for m in catalog if m.id and m.string)
        print(f"{locale}: {translated}/{len(MESSAGES)} translated")


if __name__ == "__main__":
    build()
